// Nav groups
const NAV_GROUPS = [
  { label: 'Управление', paths: ['/', '/orders', '/calculator'] },
  { label: 'Производство', paths: ['/production', '/production/design', '/production/print', '/production/resin', '/production/assembly'] },
  { label: 'Ресурсы', paths: ['/warehouse', '/clients', '/analytics'] },
  { label: 'Система', paths: ['/settings'] },
]

// Map path to count key
const COUNT_MAP = {
  '/orders': 'new',
  '/production/design': 'design',
  '/production/print': 'print',
  '/production/assembly': 'assembly',
  '/production/resin': 'resin_pouring',
}

export function Sidebar({ collapsed, onToggle }) {
  const { profile, signOut } = useAuth()
  const { theme, toggle: toggleTheme } = useThemeStore()
  const counts = useSidebarStore((s) => s.counts)
  const lowStockCount = useSidebarStore((s) => s.lowStockCount)
  const fetchCounts = useSidebarStore((s) => s.fetchCounts)
  const role = profile?.role || 'viewer'

  useEffect(() => {
    fetchCounts()
    const interval = setInterval(fetchCounts, 30000)
    return () => clearInterval(interval)
  }, [fetchCounts])

  const visibleItems = NAV_ITEMS.filter((item) => item.roles.includes(role))

  // Group items
  const groupedNav = NAV_GROUPS.map((group) => ({
    ...group,
    items: visibleItems.filter((item) => group.paths.includes(item.path)),
  })).filter((g) => g.items.length > 0)

  return (
    <aside aria-label="Боковая навигация" className={cn(
      'fixed left-0 top-0 h-screen bg-sidebar text-white flex flex-col transition-all duration-200 z-40',
      collapsed ? 'w-16' : 'w-60'
    )}>
      {/* Logo */}
      <div className="flex items-center gap-3 px-4 h-16 border-b border-white/10">
        <div className="flex-shrink-0 w-8 h-8 rounded-lg bg-accent flex items-center justify-center font-bold text-sm">
          K
        </div>
        {!collapsed && <span className="text-lg font-semibold tracking-tight">Kontora24</span>}
      </div>

      {/* Nav */}
      <nav aria-label="Основная навигация" className="flex-1 overflow-y-auto py-2 px-2">
        {groupedNav.map((group) => (
          <div key={group.label} className="mb-2">
            {!collapsed && (
              <p className="px-3 py-1.5 text-[10px] font-semibold uppercase tracking-wider text-white/60">
                {group.label}
              </p>
            )}
            {group.items.map((item) => {
              const Icon = ICONS[item.icon] || ICONS.LayoutDashboard
              const count = COUNT_MAP[item.path] ? counts[COUNT_MAP[item.path]] : null
              const hasAlert = item.path === '/warehouse' && lowStockCount > 0
              return (
                <NavLink
                  key={item.path}
                  to={item.path}
                  end={item.path === '/'}
                  className={({ isActive }) => cn(
                    'flex items-center gap-3 px-3 py-2 rounded-lg text-sm transition-colors',
                    isActive
                      ? 'bg-white/15 text-white font-medium'
                      : 'text-white/70 hover:bg-white/10 hover:text-white'
                  )}
                >
                  <span className="relative">
                    <Icon />
                    {hasAlert && (
                      <span className="absolute -top-0.5 -right-0.5 w-2 h-2 rounded-full bg-red-500" aria-label="Низкий остаток материалов" />
                    )}
                  </span>
                  {!collapsed && (
                    <>
                      <span className="flex-1">{item.label}</span>
                      {count > 0 && (
                        <span className="bg-accent text-white text-[10px] font-bold rounded-full w-5 h-5 flex items-center justify-center">
                          {count > 99 ? '99+' : count}
                        </span>
                      )}
                    </>
                  )}
                </NavLink>
              )
            })}
          </div>
        ))}
      </nav>

      {/* Footer */}
      <div className="border-t border-white/10 p-2 space-y-0.5">
        {!collapsed && profile && (
          <div className="flex items-center gap-3 px-3 py-2">
            <div className="w-8 h-8 rounded-full bg-accent/30 flex items-center justify-center text-xs font-medium">
              {profile.display_name?.[0]?.toUpperCase() || 'U'}
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-sm font-medium truncate">{profile.display_name}</p>
              <p className="text-xs text-white/70 capitalize">{profile.role}</p>
            </div>
          </div>
        )}
        <button
          onClick={toggleTheme}
          className="flex items-center gap-3 w-full px-3 py-2 rounded-lg text-sm text-white/75 hover:bg-white/10 hover:text-white transition-colors"
          aria-label="Сменить тему"
        >
          {theme === 'light' ? (
            <svg className="w-5 h-5" fill="none" stroke="currentColor" strokeWidth="1.5" viewBox="0 0 24 24"><path strokeLinecap="round" strokeLinejoin="round" d="M21.752 15.002A9.718 9.718 0 0118 15.75c-5.385 0-9.75-4.365-9.75-9.75 0-1.33.266-2.597.748-3.752A9.753 9.753 0 003 11.25C3 16.635 7.365 21 12.75 21a9.753 9.753 0 009.002-5.998z" /></svg>
          ) : (
            <svg className="w-5 h-5" fill="none" stroke="currentColor" strokeWidth="1.5" viewBox="0 0 24 24"><path strokeLinecap="round" strokeLinejoin="round" d="M12 3v2.25m6.364.386l-1.591 1.591M21 12h-2.25m-.386 6.364l-1.591-1.591M12 18.75V21m-4.773-4.227l-1.591 1.591M5.25 12H3m4.227-4.773L5.636 5.636M15.75 12a3.75 3.75 0 11-7.5 0 3.75 3.75 0 017.5 0z" /></svg>
          )}
          {!collapsed && <span>{theme === 'light' ? 'Тёмная' : 'Светлая'}</span>}
        </button>
        <button
          onClick={() => { if (window.confirm('Вы уверены, что хотите выйти?')) signOut() }}
          className="flex items-center gap-3 w-full px-3 py-2 rounded-lg text-sm text-white/75 hover:bg-white/10 hover:text-white transition-colors"
          aria-label="Выйти"
        >
          <svg className="w-5 h-5" fill="none" stroke="currentColor" strokeWidth="1.5" viewBox="0 0 24 24"><path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15M12 9l-3 3m0 0l3 3m-3-3h12.75" /></svg>
          {!collapsed && <span>Выйти</span>}
        </button>
      </div>
    </aside>
  )
}
